<p><span style="font-size: 160%;"><strong>NATIONAL INSTITUTE OF TECHNOLOGY</strong></span></p>
<div>
<p><img src="assets/images/DSC04924.JPG" alt="" width="522" height="285" /></p>
<p>The National Institutes of Technology are premier institutions of technology in India offering academic programs at undergraduate, post graduate and doctoral levels. Originally started as the Regional Engineering Colleges (RECs), they have grown in stature and are today rated among the top technological institutions of the country. In 2002, while granting them autonomy, they were taken under the ambit of Centrally Funded Technical Institutions of Ministry of Human Resource Development (MHRD) and renamed as National Institutes of Technology. Further, under the NIT Act 2007, they were elevated to the status of Institutes of National Importance.&nbsp;</p>
<p><br />Students are admitted to B. Tech programs in the NITs on the basis of ranks obtained in the All India Engineering Entrance Examination (AIEEE). While 50% of the seats are reserved for students of the state in which the particular NIT is located, the remaining 50% of seats are open for students from across the country on the basis of merit. Reservation of seats is followed as per Government of India norms.</p>
<p>&nbsp;<strong><span style="color: #ff00ff; font-size: 120%;">Location Map</span><br /></strong></p>
<p><img src="assets/images/map.JPG" alt="" width="527" height="301" /><br /><span style="color: #ff00ff; font-size: 140%;"><strong>&nbsp;</strong></span></p>
<p><span style="color: #ff00ff; font-size: 140%;"><strong>NATIONAL INSTITUTE OF TECHNOLOGY, GOA</strong></span></p>
<p>In 2009, MHRD vide its <a href="assets/files/mhrd_order_30oct09.pdf">Order No. F. 23-13-2009-TS-III dated 30th October and 3rd March 2010</a> decided to set up 10 new NITs during the 11th Five Year Plan. The <strong>National Institute of Technology, Goa </strong>is one such new NIT. Initially this Institute was looked after and mentored by NITK Surathkal.50% of the seats in this Institute are reserved for State of<strong> Goa.</strong>&nbsp;</p>
<p>At present The NITGoa is temporarily accommodated and functioning in the <a href="http://www.gec.ac.in/">Goa Engineering College (GEC)</a>&nbsp;&nbsp;Campus<a href="index.php?q=location.html"> located at Farmagudi, Goa.</a> Policy of student admissions to the new NITs is same as that for the existing NITs i.e. through AIEEE rank. The first batch of students has been admitted and academic activities of NITGoa started from 2010-11 session when the first batch was admitted. </p>
<p>To begin with, MHRD allocated an annual intake of 90 students to NIT, Goa, comprising of 30 students each in the following B. Tech programmes:</p>
<ul>
<li>Computer Science and Engineering</li>
<li>Electronics and Communication Engineering</li>
<li>Electrical and Electronics Engineering</li>
</ul>
<p>In addition, 6 seats have been allocated under the Direct Admission for Students Abroad (DASA) scheme.</p>
